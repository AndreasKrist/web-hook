/**
 * Dark Mode Functionality for Web-Hook
 * This script handles theme toggling between light and dark modes
 */

// Initialize theme toggle when DOM is fully loaded
document.addEventListener('DOMContentLoaded', function() {
    // Create and append the theme toggle button
    createThemeToggleButton();
    
    // Initialize theme based on stored preference or system preference
    initializeTheme();
});

/**
 * Creates the theme toggle button and appends it to the document
 */
